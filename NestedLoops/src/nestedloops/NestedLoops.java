
package nestedloops;


public class NestedLoops {


    public static void main(String[] args) {
        
        
        for(int i = 10; i>0; i--){
            for (int j=i; j<10; j++){
                System.out.print(" ");
            }
            System.out.println("#");
        }
            
        
        
    }
    
}
